/* =========================================================
   APPLYING THE SELECTED LANGUAGE
   ========================================================= */
function applyLanguage(lang) {
  if (!translations[lang]) lang = 'fr';
  var dict = translations[lang];

  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) el.setAttribute('placeholder', dict[key]);
  });

  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  if (typeof window.renderLevelPage === 'function') {
    window.renderLevelPage(lang);
  }

  try { localStorage.setItem('site_lang', lang); } catch (e) { /* ignore */ }
}

/* =========================================================
   INIT
   ========================================================= */
document.addEventListener('DOMContentLoaded', function () {

  /* --- language switch --- */
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.addEventListener('click', function () {
      applyLanguage(btn.getAttribute('data-lang'));
      var navLinksEl = document.getElementById('navLinks');
      var navOverlayEl = document.getElementById('navOverlay');
      if (navLinksEl) navLinksEl.classList.remove('open');
      if (navOverlayEl) navOverlayEl.classList.remove('open');
    });
  });

  var savedLang = 'fr';
  try { savedLang = localStorage.getItem('site_lang') || 'fr'; } catch (e) { /* ignore */ }
  applyLanguage(savedLang);

  /* --- mobile menu --- */
  var menuToggle = document.getElementById('menuToggle');
  var menuClose = document.getElementById('menuClose');
  var navLinks = document.getElementById('navLinks');
  var navOverlay = document.getElementById('navOverlay');

  function openMenu() {
    navLinks.classList.add('open');
    if (navOverlay) navOverlay.classList.add('open');
    if (menuToggle) menuToggle.setAttribute('aria-expanded', 'true');
  }
  function closeMenu() {
    navLinks.classList.remove('open');
    if (navOverlay) navOverlay.classList.remove('open');
    if (menuToggle) menuToggle.setAttribute('aria-expanded', 'false');
  }

  if (menuToggle && navLinks) {
    menuToggle.addEventListener('click', function () {
      if (navLinks.classList.contains('open')) closeMenu(); else openMenu();
    });
    if (menuClose) menuClose.addEventListener('click', closeMenu);
    if (navOverlay) navOverlay.addEventListener('click', closeMenu);
    navLinks.querySelectorAll('a').forEach(function (a) {
      a.addEventListener('click', closeMenu);
    });
  }

  /* --- contact form --- */
  var form = document.getElementById('inscriptionForm');
  var successMsg = document.getElementById('successMsg');
  var iframe = document.getElementById('hiddenIframe');

  if (form && iframe) {
    form.addEventListener('submit', function () {
      window.formSubmitted = true;
    });

    iframe.addEventListener('load', function () {
      if (window.formSubmitted) {
        successMsg.style.display = 'flex';
        form.reset();
        window.formSubmitted = false;
      }
    });
  }
});
